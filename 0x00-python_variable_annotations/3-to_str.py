#!/usr/bin/env python3
"""
type-annotated function to_str
- Takes a float n as argument
- Returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of float.

    parameter(s):
    - n (float): The input float.

    Returns:
    - str: the string representation of input float.
    """
    return str(n)
